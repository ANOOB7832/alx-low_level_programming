#include <stdio.h>
#include "main.h"
/**
 * print_diagonal - prints a diagonal line of length n
 * @n: length of diagonal line
 * Return: void
 */
void print_diagonal(int n);
{
	int a, b;

	if (n <= 0)
	{
		_putchar('\n');
		for (a = 0; b < a; b++)
		{
			_putchar(' ');
		}
	_putchar('\\');
	_putchar('\n');
	}
}

